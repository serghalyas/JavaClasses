package com.java.class03;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        System.out.println (num1+num2);
        System.out.println (num1-num2);
        System.out.println (num1*num2);
        System.out.println (num1/num2);

        // Get Last digit from the number

        System.out.println(10%3);
        System.out.println(15%4);
        System.out.println(20%2);
        System.out.println(26%2);

        // Remove last digit from the number

        System.out.println(15/10);
        System.out.println(67/10);
        System.out.println(133/10);
        System.out.println(265/10);
        System.out.println(2655/10);





    }


}
